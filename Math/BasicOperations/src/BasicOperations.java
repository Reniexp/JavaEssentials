public class BasicOperations {
    public static class addition {
        public int execute(int a, int b) {
            return a + b;
        }
    }
    public static class Subtraction {
        public int execute(int a, int b) {
            return a - b;
        }
    }
    public static class Multiplication {
        public int execute(int a, int b) {
            return a * b;
        }
    }
}
